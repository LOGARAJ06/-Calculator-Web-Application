//boolean value
let isActive = true; // variable name mentioned in camel case
console.log(isActive); // Output: true
console.log(typeof isActive); // Output: boolean

//undefined value
let notDefined; // variable not initialized
console.log(notDefined); // Output: undefined
console.log(typeof notDefined); // Output: undefined

//NUll value
let emptyValue = null; // variable explicitly set to null
console.log(emptyValue); // Output: null

//dynamic typing
let dynamicVar = 42; // Initially a number
console.log(dynamicVar); // Output: 42
dynamicVar = "Now I'm a string"; // Now a string
console.log(dynamicVar); // Output: Now I'm a string
